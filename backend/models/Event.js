// models/Event.js
import mongoose from 'mongoose'; // âœ… correct


const eventSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
    trim: true
  },
  description: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    required: true
  },
  location: {
    type: String,
    required: true
  },
  capacity: {
    type: Number,
    required: true,
    min: 1
  }
}, {
  timestamps: true
});

export default mongoose.model('Event', eventSchema);
